#!/usr/bin/env bash
set -euo pipefail

CONFIG="configs/geo_vector_lora.yaml"
CHECKPOINT=""
SPLIT="val"
OUTPUT="outputs/geo_eval_metrics.json"
MAX_SAMPLES="0"
DATASET_ROOT="/home/zsy/Downloads/dataset-extracted"
DINO_MODEL_PATH=""
QWEN_MODEL_PATH=""
DEVICE=""
IMAGE_SIZE="448"
TILE_SIZE_PX="1024"
OVERLAP_PX="256"
KEEP_MARGIN_PX="128"
STATE_BORDER_MARGIN_PX="128"
SAMPLE_INTERVAL_METER="6.0"
COORD_BINS="1024"
LANE_MAX_FEATURES="0"
INTERSECTION_MAX_FEATURES="0"
STATE_MAX_FEATURES="32"
STATE_ANCHOR_MAX_POINTS="6"
PROMPT_MAX_TOKENS="0"
STATE_MAX_TOKENS="0"
TARGET_MAX_TOKENS="0"
MAX_NEW_TOKENS="0"
MIN_NEW_TOKENS="0"
MAX_PROP_TOKENS="0"
TEMPERATURE="1.0"
TOP_K="1"
REPETITION_PENALTY="1.02"
USE_KV_CACHE="true"
ARTIFACT_EXPORT_ENABLED="true"
SAVE_KEPT_PATCHES="true"
SAVE_DISCARDED_PATCHES="true"
SAVE_RESIZED_PATCH_INPUTS="true"
SAVE_EVAL_SAMPLE_GEOJSON="true"
SAVE_PREDICT_TILE_GEOJSON="true"
ARTIFACT_MAX_PATCH_IMAGES_PER_SAMPLE="0"

set_env_if_value() {
  local name="$1"
  local value="$2"
  if [[ -n "$value" ]]; then
    export "$name=$value"
  fi
}

if [[ -z "$CHECKPOINT" ]]; then
  echo "Checkpoint is required." >&2
  exit 1
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/.." && pwd)"
cd "$REPO_ROOT"

set_env_if_value UNIMAPGEN_GEO_ROOT "$DATASET_ROOT"
set_env_if_value DINOV2_BACKBONE_PATH "$DINO_MODEL_PATH"
set_env_if_value QWEN_MODEL_PATH "$QWEN_MODEL_PATH"
set_env_if_value UNIMAPGEN_DEVICE "$DEVICE"
set_env_if_value UNIMAPGEN_IMAGE_SIZE "$IMAGE_SIZE"
set_env_if_value UNIMAPGEN_TILE_SIZE_PX "$TILE_SIZE_PX"
set_env_if_value UNIMAPGEN_OVERLAP_PX "$OVERLAP_PX"
set_env_if_value UNIMAPGEN_KEEP_MARGIN_PX "$KEEP_MARGIN_PX"
set_env_if_value UNIMAPGEN_STATE_BORDER_MARGIN_PX "$STATE_BORDER_MARGIN_PX"
set_env_if_value UNIMAPGEN_SAMPLE_INTERVAL_METER "$SAMPLE_INTERVAL_METER"
set_env_if_value UNIMAPGEN_COORD_BINS "$COORD_BINS"
set_env_if_value UNIMAPGEN_LANE_MAX_FEATURES "$LANE_MAX_FEATURES"
set_env_if_value UNIMAPGEN_INTERSECTION_MAX_FEATURES "$INTERSECTION_MAX_FEATURES"
set_env_if_value UNIMAPGEN_STATE_MAX_FEATURES "$STATE_MAX_FEATURES"
set_env_if_value UNIMAPGEN_STATE_ANCHOR_MAX_POINTS "$STATE_ANCHOR_MAX_POINTS"
set_env_if_value UNIMAPGEN_PROMPT_MAX_TOKENS "$PROMPT_MAX_TOKENS"
set_env_if_value UNIMAPGEN_STATE_MAX_TOKENS "$STATE_MAX_TOKENS"
set_env_if_value UNIMAPGEN_TARGET_MAX_TOKENS "$TARGET_MAX_TOKENS"
set_env_if_value UNIMAPGEN_MAX_NEW_TOKENS "$MAX_NEW_TOKENS"
set_env_if_value UNIMAPGEN_MIN_NEW_TOKENS "$MIN_NEW_TOKENS"
set_env_if_value UNIMAPGEN_MAX_PROP_TOKENS "$MAX_PROP_TOKENS"
set_env_if_value UNIMAPGEN_TEMPERATURE "$TEMPERATURE"
set_env_if_value UNIMAPGEN_TOP_K "$TOP_K"
set_env_if_value UNIMAPGEN_REPETITION_PENALTY "$REPETITION_PENALTY"
set_env_if_value UNIMAPGEN_USE_KV_CACHE "$USE_KV_CACHE"
set_env_if_value UNIMAPGEN_ARTIFACT_EXPORT_ENABLED "$ARTIFACT_EXPORT_ENABLED"
set_env_if_value UNIMAPGEN_SAVE_KEPT_PATCHES "$SAVE_KEPT_PATCHES"
set_env_if_value UNIMAPGEN_SAVE_DISCARDED_PATCHES "$SAVE_DISCARDED_PATCHES"
set_env_if_value UNIMAPGEN_SAVE_RESIZED_PATCH_INPUTS "$SAVE_RESIZED_PATCH_INPUTS"
set_env_if_value UNIMAPGEN_SAVE_EVAL_SAMPLE_GEOJSON "$SAVE_EVAL_SAMPLE_GEOJSON"
set_env_if_value UNIMAPGEN_SAVE_PREDICT_TILE_GEOJSON "$SAVE_PREDICT_TILE_GEOJSON"
set_env_if_value UNIMAPGEN_ARTIFACT_MAX_PATCH_IMAGES_PER_SAMPLE "$ARTIFACT_MAX_PATCH_IMAGES_PER_SAMPLE"

args=(
  -m unimapgen.eval_geo_vector
  --config "$CONFIG"
  --checkpoint "$CHECKPOINT"
  --split "$SPLIT"
  --output "$OUTPUT"
)
if [[ "$MAX_SAMPLES" != "0" ]]; then
  args+=(--max_samples "$MAX_SAMPLES")
fi

python "${args[@]}"
